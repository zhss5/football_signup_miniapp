const cloud = require('wx-server-sdk');
const { nowIso } = require('./_shared/time');
const { validateActivityDraft } = require('./_shared/validators');

const BENCH_TEAM_NAME = '\u66ff\u8865';

cloud.init({ env: cloud.DYNAMIC_CURRENT_ENV });

function buildTeamDrafts(event) {
  const regularTeams = event.teams.map((team, index) => ({
    teamName: team.teamName.trim(),
    sort: index,
    maxMembers: Number(team.maxMembers) || 0,
    teamType: 'regular',
    autoGenerated: false
  }));
  const teamSlots = regularTeams.reduce((sum, team) => sum + team.maxMembers, 0);
  const benchSlots = Math.max((Number(event.signupLimitTotal) || 0) - teamSlots, 0);

  if (benchSlots > 0) {
    regularTeams.push({
      teamName: BENCH_TEAM_NAME,
      sort: regularTeams.length,
      maxMembers: benchSlots,
      teamType: 'bench',
      autoGenerated: true
    });
  }

  return regularTeams;
}

async function main(event, context = cloud.getWXContext(), deps = {}) {
  const db = deps.db || cloud.database();
  validateActivityDraft(event);
  const teams = buildTeamDrafts(event);

  const stamp = nowIso(deps.now);
  const imageList = Array.isArray(event.imageList)
    ? event.imageList.filter(Boolean)
    : event.coverImage
      ? [event.coverImage]
      : [];

  const activityData = {
    title: event.title.trim(),
    organizerOpenId: context.OPENID,
    startAt: event.startAt,
    endAt: event.endAt,
    signupDeadlineAt: event.signupDeadlineAt,
    addressText: event.addressText.trim(),
    addressName: event.addressName || event.addressText.trim(),
    location: event.location || null,
    description: event.description || '',
    coverImage: imageList[0] || event.coverImage || '',
    imageList,
    signupLimitTotal: Number(event.signupLimitTotal) || 0,
    joinedCount: 0,
    requirePhone: Boolean(event.requirePhone),
    inviteCode: event.inviteCode || '',
    feeMode: 'free',
    status: 'published',
    createdAt: stamp,
    updatedAt: stamp
  };

  const activityRes = await db.collection('activities').add({ data: activityData });

  for (let index = 0; index < teams.length; index += 1) {
    await db.collection('activity_teams').add({
      data: {
        activityId: activityRes._id,
        teamName: teams[index].teamName,
        sort: teams[index].sort,
        maxMembers: teams[index].maxMembers,
        joinedCount: 0,
        teamType: teams[index].teamType,
        autoGenerated: teams[index].autoGenerated,
        status: 'active',
        createdAt: stamp
      }
    });
  }

  return { activityId: activityRes._id };
}

module.exports = { main };
